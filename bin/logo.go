func main() {
	width, height := 100, 100
	img := image.NewRGBA(image.Rect(0, 0, width, height))
	const cellSize = 20

	// Define the pixel art for the gopher
	matrix := generateGopherMatrix()
	white := color.RGBA{255, 255, 255, 255}
	black := color.RGBA{0, 0, 0, 255}
	gopherBlue := color.RGBA{173, 216, 230, 255}
	skin := color.RGBA{245, 209, 132, 255}
	const w = 0
	const b = 1
	const bk = 2
	const sk = 3

	// Apply the matrix to the image
	for y := 0; y < height; y++ {
		for x := 0; x < width; x++ {
			c := matrix[y][x]
			switch c {
			case w:
				img.Set(x, y, white)
			case b:
				img.Set(x, y, gopherBlue)
			case bk:
				img.Set(x, y, black)
			case sk:
				img.Set(x, y, skin)
			}
			//img.Set(x, y, color.RGBA{R: c[0], G: c[1], B: c[2], A: c[3]})
		}
	}

	// Save the image as a PNG file
	file, _ := os.Create("gopher_logo.png")
	defer file.Close()
	png.Encode(file, img)
}

